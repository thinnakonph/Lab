// เลข 255
const number = 255;

// แปลงเลข 255 เป็น binary
const binaryValue = number.toString(2);
console.log(`255 in binary: ${binaryValue}`);

// แปลงเลข 255 เป็น octal
const octalValue = number.toString(8);
console.log(`255 in octal: ${octalValue}`);

// แปลงเลข 255 เป็น hexadecimal
const hexValue = number.toString(16).toUpperCase();
console.log(`255 in hexadecimal: ${hexValue}`);
